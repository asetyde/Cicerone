    private String returnCommands(String text) {
        String splitting[] = text.split("\r");
        text = "";

        for (int i = 0; i < splitting.length; i++) {
            if (!splitting[i].equals(" ")) {
                text += splitting[i] + " ";
            }

        }
        splitting = text.split("\n");
        text = "";

        for (int i = 0; i < splitting.length; i++) {
            if (!splitting[i].equals(" ")) {
                text += splitting[i] + " ";
            }
        }
        splitting = text.split("\t");
        text = "";
        for (int i = 0; i < splitting.length; i++) {
            text += splitting[i] + " ";
        }

        splitting = text.split(" ");
        text = "";
        for (int i = 0; i < splitting.length; i++) {
            if (!splitting[i].equals("")) {
                text += splitting[i] + " ";
            }
        }

        return text;
    }

